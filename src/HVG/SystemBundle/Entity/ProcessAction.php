     * Set process
     *
     * @param \HVG\SystemBundle\Entity\Process $process
     *
     * @return ProcessAction
     */
    public function setProcess(\HVG\SystemBundle\Entity\Process $process = null)
    {
        $this->process = $process;

        return $this;
    }

    /**
     * Get process
     *
     * @return \HVG\SystemBundle\Entity\Process
     */
    public function getProcess()
    {
        return $this->process;
    }
